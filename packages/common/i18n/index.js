  function detectLang() {
    var q = new URLSearchParams(window.location.search).get('lang');
    if (q === 'zh' || q === 'en') return q;
    var nav = (navigator.language || '').toLowerCase();
    if (nav.startsWith('zh')) return 'zh';
    return 'en';
  }

  function load(langOverride) {
    var target = langOverride || detectLang();
    lang = target;
    loaded = true;

    callbacks.forEach(function (cb) {
      cb(lang, DICT[lang] || {});
    });
    callbacks = [];
  }

  function setLang(langOverride) {
    lang = langOverride || 'zh';
    loaded = true;
  }

  function t(path) {
    var dict = DICT[lang] || DICT.zh;
    var val = dict[path];
    return val !== undefined && val !== null ? val : path;
  }

  function onReady(cb) {
    if (loaded) {
      cb(lang, DICT[lang] || {});
    } else {
      callbacks.push(cb);
    }
  }

  function getLang() {
    return lang;
  }

  return {
    load: load,
    setLang: setLang,
    t: t,
    onReady: onReady,
    getLang: getLang,
  };
